func (i *appInvoker) Revert(ipc.ServerContext) error {
	installationDir, err := i.installationDir()
	if err != nil {
		return err
	}
	if !installationStateIs(installationDir, active) {
		return errInvalidOperation
	}
	// NOTE(caprita): A race can occur between an update and a revert, where
	// both use the same current version as their starting point.  This will
	// render the update inconsequential.  This doesn't appear to be of
	// practical concern, so we avoid the complexity of synchronizing
	// updates and revert operations.
	currLink := filepath.Join(installationDir, "current")
	currVersionDir, err := filepath.EvalSymlinks(currLink)
	if err != nil {
		vlog.Errorf("EvalSymlinks(%v) failed: %v", currLink, err)
		return errOperationFailed
	}
	previousLink := filepath.Join(currVersionDir, "previous")
	if _, err := os.Lstat(previousLink); err != nil {
		if os.IsNotExist(err) {
			// No 'previous' link -- must be the first version.
			return errUpdateNoOp
		}
		vlog.Errorf("Lstat(%v) failed: %v", previousLink, err)
		return errOperationFailed
	}
	prevVersionDir, err := filepath.EvalSymlinks(previousLink)
	if err != nil {
		vlog.Errorf("EvalSymlinks(%v) failed: %v", previousLink, err)
		return errOperationFailed
	}
	return updateLink(prevVersionDir, currLink)
}

type treeNode struct {
	children map[string]*treeNode
	remote   string // Used to implement Glob for remote objects.
}

func newTreeNode() *treeNode {
	return &treeNode{children: make(map[string]*treeNode)}
}

func (n *treeNode) find(names []string, create bool) *treeNode {
	for {
		if len(names) == 0 {
			return n
		}
		if next, ok := n.children[names[0]]; ok {
			n = next
			names = names[1:]
			continue
		}
		if create {
			nn := newTreeNode()
			n.children[names[0]] = nn
			n = nn
			names = names[1:]
			continue
		}
		return nil
	}
}

// scanConfigDir scans the config directory to build tree representation of all
// the valid object names.
func (i *appInvoker) scanConfigDir() *treeNode {
	tree := newTreeNode()

	// appIDMap[appID]title
	appIDMap := make(map[string]string)

	// Find all envelopes, extract appID and installID.
	envGlob := []string{i.config.Root, "app-*", "installation-*", "*", "envelope"}
	envelopes, err := filepath.Glob(filepath.Join(envGlob...))
	if err != nil {
		vlog.Errorf("unexpected error: %v", err)
		return nil
	}
	for _, path := range envelopes {
		env, err := loadEnvelope(filepath.Dir(path))
		if err != nil {
			continue
		}
		relpath, _ := filepath.Rel(i.config.Root, path)
		elems := strings.Split(relpath, string(filepath.Separator))
		if len(elems) != len(envGlob)-1 {
			vlog.Errorf("unexpected number of path components: %q (%q)", elems, path)
			continue
		}
		appID := strings.TrimPrefix(elems[0], "app-")
		installID := strings.TrimPrefix(elems[1], "installation-")
		appIDMap[appID] = env.Title
		tree.find([]string{env.Title, installID}, true)
	}

	// Find all instances.
	infoGlob := []string{i.config.Root, "app-*", "installation-*", "instances", "instance-*", "info"}
	instances, err := filepath.Glob(filepath.Join(infoGlob...))
	if err != nil {
		vlog.Errorf("unexpected error: %v", err)
		return nil
	}
	for _, path := range instances {
		instanceDir := filepath.Dir(path)
		info, err := loadInstanceInfo(instanceDir)
		if err != nil {
			continue
		}
		relpath, _ := filepath.Rel(i.config.Root, path)
		elems := strings.Split(relpath, string(filepath.Separator))
		if len(elems) != len(infoGlob)-1 {
			vlog.Errorf("unexpected number of path components: %q (%q)", elems, path)
			continue
		}
		appID := strings.TrimPrefix(elems[0], "app-")
		installID := strings.TrimPrefix(elems[1], "installation-")
		instanceID := strings.TrimPrefix(elems[3], "instance-")
		if title, ok := appIDMap[appID]; ok {
			n := tree.find([]string{title, installID, instanceID, "logs"}, true)
			i.addLogFiles(n, filepath.Join(instanceDir, "logs"))

			if instanceStateIs(instanceDir, started) {
				// Set the name of the remote objects that should handle Glob.
				for _, obj := range []string{"pprof", "stats"} {
					n = tree.find([]string{title, installID, instanceID, obj}, true)
					n.remote = naming.JoinAddressName(info.AppCycleMgrName, naming.Join("__debug", obj))
				}
			}
		}
	}
	return tree
}

func (i *appInvoker) addLogFiles(n *treeNode, dir string) {
	filepath.Walk(dir, func(path string, _ os.FileInfo, _ error) error {
		if path == dir {
			// Skip the logs directory itself.
			return nil
		}
		relpath, _ := filepath.Rel(dir, path)
		n.find(strings.Split(relpath, string(filepath.Separator)), true)
		return nil
	})
}

func (i *appInvoker) Glob(ctx ipc.ServerContext, pattern string, stream mounttable.GlobbableServiceGlobStream) error {
	g, err := glob.Parse(pattern)
	if err != nil {
		return err
	}
	n := i.scanConfigDir().find(i.suffix, false)
	if n == nil {
		return errInvalidSuffix
	}
	i.globStep(ctx, "", g, n, stream)
	return nil
}

func (i *appInvoker) globStep(ctx ipc.ServerContext, prefix string, g *glob.Glob, n *treeNode, stream mounttable.GlobbableServiceGlobStream) {
	if n.remote != "" {
		remoteGlob(ctx, n.remote, prefix, g.String(), stream)
		return
	}
	if g.Len() == 0 {
		stream.SendStream().Send(types.MountEntry{Name: prefix})
	}
	if g.Finished() {
		return
	}
	for name, child := range n.children {
		if ok, _, left := g.MatchInitialSegment(name); ok {
			i.globStep(ctx, naming.Join(prefix, name), left, child, stream)
		}
	}
}

func remoteGlob(ctx ipc.ServerContext, remote, prefix, pattern string, stream mounttable.GlobbableServiceGlobStream) {
	c, err := mounttable.BindGlobbable(remote)
	if err != nil {
		vlog.VI(1).Infof("BindGlobbable(%q): %v", remote, err)
		return
	}
	call, err := c.Glob(ctx, pattern)
	if err != nil {
		vlog.VI(1).Infof("%q.Glob(%q): %v", remote, pattern, err)
		return
	}
	it := call.RecvStream()
	sender := stream.SendStream()
	for it.Advance() {
		me := it.Value()
		me.Name = naming.Join(prefix, me.Name)
		sender.Send(me)
	}
	if err := it.Err(); err != nil {
		vlog.VI(1).Infof("%q.Glob(%q): %v", remote, pattern, err)
		return
	}
	if err := call.Finish(); err != nil {
		vlog.VI(1).Infof("%q.Glob(%q): %v", remote, pattern, err)
		return
	}
}
